import BookNow from "~/components/Shared/BookNow.vue";
import EasyBookingFrame from "~/components/Shared/EasyBookingFrame.vue";
defineProps<{
  url: string,
  title?: string
}>()

export default {
  components: {
    AppModal,
    BookNow,
    EasyBookingFrame,
  },
  props: {
    title: {
      type: String,
      required: true,
    },
    bookUrl: {
      type: String,
      default: null,
    },
  },
  data() {
    return {
      showModal: false,
    };
  },
  methods: {
    toggleModal() {
      this.showModal = !this.showModal;
    },
  },
